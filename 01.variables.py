# Introduction to Variables and Data Types in Python

# A variable is like a "box" where we can store data.
# In Python, a variable can store different types of data.
# These are called "data types".

# Common basic data types:
# - Integer: Whole numbers like 1, 2, 100
# - Float: Decimal numbers like 3.14, 2.5
# - String: Text like "hello", "Python"
# - Boolean: True or False (used for decisions)

# Example of each data type:

# Integer example
age = 25
print("Age:", age)

# Float (decimal) example
price = 19.99
print("Price:", price)

# String example
name = "Alex"
print("Name:", name)

# Boolean example
is_student = True
print("Is student:", is_student)

# Note: You don't need to declare the type explicitly in Python.
# Python automatically understands the type based on the value you assign.

